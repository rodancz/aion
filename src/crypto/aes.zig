// Round constant
const RCON: [10]u8 = [_]u8{ 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36 };

fn gf_mul(a: u8, b: u8) u8 {
    var p: u8 = 0;
    var x = a;
    var y = b;
    while (y != 0) {
        if (y & 1 != 0) p ^= x;
        y >>= 1;
        const hi = x & 0x80;
        x <<= 1;
        if (hi != 0) x ^= 0x1b;
    }
    return p;
}

pub fn key_expand_128(key: []const u8, round_keys: []u32) void {
    var i: usize = 0;
    while (i < 4) : (i += 1) {
        round_keys[i] = (@as(u32, key[4 * i]) << 24) | (@as(u32, key[4 * i + 1]) << 16) | (@as(u32, key[4 * i + 2]) << 8) | key[4 * i + 3];
    }

    i = 4;
    while (i < 44) : (i += 1) {
        var temp = round_keys[i - 1];
        if (i % 4 == 0) {
            temp = sub_word(rot_word(temp)) ^ (@as(u32, RCON[i / 4 - 1]) << 24);
        }
        round_keys[i] = round_keys[i - 4] ^ temp;
    }
}

fn sub_word(w: u32) u32 {
    const b0: u8 = @truncate(w >> 24);
    const b1: u8 = @truncate((w >> 16) & 0xFF);
    const b2: u8 = @truncate((w >> 8) & 0xFF);
    const b3: u8 = @truncate(w & 0xFF);
    return (@as(u32, SBOX[b0]) << 24) |
        (@as(u32, SBOX[b1]) << 16) |
        (@as(u32, SBOX[b2]) << 8) |
        SBOX[b3];
}

fn rot_word(w: u32) u32 {
    return (w << 8) | (w >> 24);
}

pub fn aes_encrypt_block(block: []u8, round_keys: []const u32) void {
    // Initial AddRoundKey
    add_round_key(block, round_keys, 0);

    var round: usize = 1;
    while (round < 10) : (round += 1) {
        sub_bytes(block);
        shift_rows(block);
        mix_columns(block);
        add_round_key(block, round_keys, round);
    }

    // Final round
    sub_bytes(block);
    shift_rows(block);
    add_round_key(block, round_keys, 10);
}

pub fn aes_decrypt_block(block: []u8, round_keys: []const u32) void {
    add_round_key(block, round_keys, 10);

    var round: usize = 9;
    while (round > 0) : (round -= 1) {
        inv_shift_rows(block);
        inv_sub_bytes(block);
        add_round_key(block, round_keys, round);
        inv_mix_columns(block);
    }

    inv_shift_rows(block);
    inv_sub_bytes(block);
    add_round_key(block, round_keys, 0);
}

fn add_round_key(state: []u8, round_keys: []const u32, round: usize) void {
    var c: usize = 0;
    while (c < 4) : (c += 1) {
        const rk = round_keys[round * 4 + c];
        state[c * 4] ^= @truncate(rk >> 24);
        state[c * 4 + 1] ^= @truncate((rk >> 16) & 0xFF);
        state[c * 4 + 2] ^= @truncate((rk >> 8) & 0xFF);
        state[c * 4 + 3] ^= @truncate(rk & 0xFF);
    }
}

fn sub_bytes(state: []u8) void {
    var i: usize = 0;
    while (i < 16) : (i += 1) state[i] = SBOX[state[i]];
}

fn inv_sub_bytes(state: []u8) void {
    var i: usize = 0;
    while (i < 16) : (i += 1) state[i] = INV_SBOX[state[i]];
}

fn shift_rows(state: []u8) void {
    // Row 1: shift left by 1
    var tmp: u8 = state[1];
    state[1] = state[5]; state[5] = state[9]; state[9] = state[13]; state[13] = tmp;
    // Row 2: shift left by 2
    tmp = state[2]; state[2] = state[10]; state[10] = tmp;
    tmp = state[6]; state[6] = state[14]; state[14] = tmp;
    // Row 3: shift left by 3 (= right by 1)
    tmp = state[15];
    state[15] = state[11]; state[11] = state[7]; state[7] = state[3]; state[3] = tmp;
}

fn inv_shift_rows(state: []u8) void {
    // Row 1: shift right by 1
    var tmp: u8 = state[13];
    state[13] = state[9]; state[9] = state[5]; state[5] = state[1]; state[1] = tmp;
    // Row 2: shift right by 2
    tmp = state[2]; state[2] = state[10]; state[10] = tmp;
    tmp = state[6]; state[6] = state[14]; state[14] = tmp;
    // Row 3: shift right by 3 (= left by 1)
    tmp = state[3];
    state[3] = state[7]; state[7] = state[11]; state[11] = state[15]; state[15] = tmp;
}

fn mix_columns(state: []u8) void {
    var c: usize = 0;
    while (c < 4) : (c += 1) {
        const s0 = state[c * 4];
        const s1 = state[c * 4 + 1];
        const s2 = state[c * 4 + 2];
        const s3 = state[c * 4 + 3];
        state[c * 4] = gf_mul(2, s0) ^ gf_mul(3, s1) ^ s2 ^ s3;
        state[c * 4 + 1] = s0 ^ gf_mul(2, s1) ^ gf_mul(3, s2) ^ s3;
        state[c * 4 + 2] = s0 ^ s1 ^ gf_mul(2, s2) ^ gf_mul(3, s3);
        state[c * 4 + 3] = gf_mul(3, s0) ^ s1 ^ s2 ^ gf_mul(2, s3);
    }
}

fn inv_mix_columns(state: []u8) void {
    var c: usize = 0;
    while (c < 4) : (c += 1) {
        const s0 = state[c * 4];
        const s1 = state[c * 4 + 1];
        const s2 = state[c * 4 + 2];
        const s3 = state[c * 4 + 3];
        state[c * 4] = gf_mul(0x0e, s0) ^ gf_mul(0x0b, s1) ^ gf_mul(0x0d, s2) ^ gf_mul(0x09, s3);
        state[c * 4 + 1] = gf_mul(0x09, s0) ^ gf_mul(0x0e, s1) ^ gf_mul(0x0b, s2) ^ gf_mul(0x0d, s3);
        state[c * 4 + 2] = gf_mul(0x0d, s0) ^ gf_mul(0x09, s1) ^ gf_mul(0x0e, s2) ^ gf_mul(0x0b, s3);
        state[c * 4 + 3] = gf_mul(0x0b, s0) ^ gf_mul(0x0d, s1) ^ gf_mul(0x09, s2) ^ gf_mul(0x0e, s3);
    }
}

pub fn cbc_encrypt(input: []u8, output: []u8, iv: []const u8, round_keys: []const u32) void {
    var block: [16]u8 = undefined;
    var i: usize = 0;
    while (i < 16) : (i += 1) block[i] = iv[i];

    var off: usize = 0;
    while (off + 16 <= input.len) : (off += 16) {
        var j: usize = 0;
        while (j < 16) : (j += 1) block[j] ^= input[off + j];
        aes_encrypt_block(block[0..], round_keys);
        j = 0;
        while (j < 16) : (j += 1) output[off + j] = block[j];
    }
}

pub fn cbc_decrypt(input: []u8, output: []u8, iv: []const u8, round_keys: []const u32) void {
    var prev: [16]u8 = undefined;
    var block: [16]u8 = undefined;
    var i: usize = 0;
    while (i < 16) : (i += 1) prev[i] = iv[i];

    var off: usize = 0;
    while (off + 16 <= input.len) : (off += 16) {
        var j: usize = 0;
        while (j < 16) : (j += 1) block[j] = input[off + j];
        aes_decrypt_block(input[off..][0..16], round_keys);
        j = 0;
        while (j < 16) : (j += 1) output[off + j] = input[off + j] ^ prev[j];
        j = 0;
        while (j < 16) : (j += 1) prev[j] = block[j];
    }
}
