const serial = @import("serial.zig");

const FONT_W: usize = 8;
const FONT_H: usize = 16;

var fb: ?[*]volatile u32 = null;
var fb_pitch: usize = 0;
var fb_cols: usize = 80;
var fb_rows: usize = 25;
var cursor_x: usize = 0;
var cursor_y: usize = 0;
var bg: u32 = 0xFF000000;
var fg: u32 = 0xFFFFFFFF;
var active: bool = false;

pub fn init(addr: u64, pitch: u32, width: u32, height: u32, bpp: u8) void {
    _ = bpp;
    if (addr == 0) return;
    const p: [*]volatile u32 = @ptrFromInt(@as(usize, @truncate(addr)));
    fb = p;
    fb_pitch = pitch / 4;
    fb_cols = width / FONT_W;
    fb_rows = height / FONT_H;

    var y: usize = 0;
    while (y < height) : (y += 1) {
        var x: usize = 0;
        while (x < fb_pitch) : (x += 1) {
            fb.?[x + y * fb_pitch] = bg;
        }
    }
    active = true;
}

fn glyph(c: u8) [16]u8 {
    if (c >= 32 and c <= 126) return FONT[c - 32];
    return FONT[0];
}

fn draw_char(col: usize, row: usize, ch: u8) void {
    if (!active) return;
    if (ch < 32 or ch > 126) return;
    const g = glyph(ch);
    const px_x = col * FONT_W;
    const px_y = row * FONT_H;

    var y: usize = 0;
    while (y < FONT_H) : (y += 1) {
        const line = g[y];
        var x: usize = 0;
        while (x < FONT_W) : (x += 1) {
            const bit = (line >> @as(u3, @truncate(7 - x))) & 1;
            const color: u32 = if (bit != 0) fg else bg;
            fb.?[px_x + x + (px_y + y) * fb_pitch] = color;
        }
    }
}

pub fn put_char(c: u8) void {
    if (!active) return;

    if (c == '\n') {
        cursor_x = 0;
        cursor_y += 1;
    } else if (c == '\r') {
        cursor_x = 0;
    } else if (c == '\x08') {
        if (cursor_x > 0) {
            cursor_x -= 1;
            draw_char(cursor_x, cursor_y, ' ');
        }
    } else {
        draw_char(cursor_x, cursor_y, c);
        cursor_x += 1;
    }

    if (cursor_x >= fb_cols) {
        cursor_x = 0;
        cursor_y += 1;
    }
    if (cursor_y >= fb_rows) {
        scroll();
        cursor_y = fb_rows - 1;
    }
}

fn scroll() void {
    if (!active) return;
    var y: usize = FONT_H;
    while (y < fb_rows * FONT_H) : (y += 1) {
        var x: usize = 0;
        while (x < fb_pitch) : (x += 1) {
            fb.?[x + (y - FONT_H) * fb_pitch] = fb.?[x + y * fb_pitch];
        }
    }
    var y2: usize = (fb_rows - 1) * FONT_H;
    while (y2 < fb_rows * FONT_H) : (y2 += 1) {
        var x: usize = 0;
        while (x < fb_pitch) : (x += 1) {
            fb.?[x + y2 * fb_pitch] = bg;
        }
    }
}

pub fn write_str(s: []const u8) void {
    for (s) |c| put_char(c);
}

pub fn clear() void {
    if (!active) return;
    cursor_x = 0;
    cursor_y = 0;
    var y: usize = 0;
    while (y < fb_rows * FONT_H) : (y += 1) {
        var x: usize = 0;
        while (x < fb_pitch) : (x += 1) {
            fb.?[x + y * fb_pitch] = bg;
        }
    }
}

